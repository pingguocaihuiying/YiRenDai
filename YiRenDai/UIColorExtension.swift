import UIKit

extension UIColor{
    
    /**
     * 获取颜色 -- 灰色 -- UIColor(red: 0.80, green: 0.80, blue: 0.80, alpha: 1.0)
     */
    class func getGrayColorFirst() -> UIColor{
        return UIColor(red: 0.80, green: 0.80, blue: 0.80, alpha: 1.0)
    }
    
    /**
     * 获取颜色 -- 灰色 -- UIColor(red:0.90, green:0.90, blue:0.90, alpha:1.00)
     */
    class func getGrayColorSecond() -> UIColor{
        return UIColor(red:0.90, green:0.90, blue:0.90, alpha:1.00)
    }
    
    /**
     * 获取颜色 -- 灰色 -- UIColor(red: 0.96, green: 0.96, blue: 0.96, alpha: 1.0)
     */
    class func getGrayColorThird() -> UIColor{
        return UIColor(red: 0.96, green: 0.96, blue: 0.96, alpha: 1.0)
    }
    
    /**
     * 获取颜色 -- 红色 -- UIColor(red:0.99, green:0.33, blue:0.17, alpha:1.00)
     */
    class func getRedColorFirst() -> UIColor{
        return UIColor(red:0.99, green:0.33, blue:0.17, alpha:1.00)
    }//UIColor(red:0.95, green:0.51, blue:0.09, alpha:1.00)
    
    /**
     * 获取颜色 -- 红色 -- UIColor(red:0.95, green:0.51, blue:0.09, alpha:1.00)
     */
    class func getRedColorSecond() -> UIColor{
        return UIColor(red:0.95, green:0.51, blue:0.09, alpha:1.00)
    }
}
